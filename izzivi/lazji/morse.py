def text_v_morse(text):
    """Vnesi besedo (crke in stevilke) in pretvori v morse."""
    text = text.upper()
    morse = ""

    for znak in text:
        morse += CODE[znak] + "   "

    morse = morse[:-1]  # zbrisi zadnji presledek!
    return morse


def morse_v_text(morse):
    """Vnesi morse - crke locene s presledki - in pretvori v besedo"""
    seznam_morse = morse.split(" ")
    seznam_text = []

    for crka in seznam_morse:
        seznam_text.append(CODE_REVERSED[crka])

    text = "".join(seznam_text)
    return text


